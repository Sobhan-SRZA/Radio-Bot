/**
 *
 * @param {Array} array
 * @param {number} chunkSize
 * @returns {Array<Array>}
 */
module.exports = function (array, chunkSize) {
  const result = [];
  for (let i = 0; i < array.length; i += chunkSize) {
    result.push(array.slice(i, i + chunkSize));
  }
  return result;
}
